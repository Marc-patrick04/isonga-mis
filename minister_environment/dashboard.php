    $stmt = $pdo->query("
        SELECT COUNT(*) as security_incidents 
        FROM tickets 
        WHERE category_id = 5 
        AND created_at >= CURRENT_DATE - INTERVAL '30 days'
    ");
    $security_incidents = $stmt->fetch(PDO::FETCH_ASSOC)['security_incidents'] ?? 0;
    
