    // Recent environmental projects (PostgreSQL uses INTERVAL)
    $stmt = $pdo->prepare("
        SELECT * FROM innovation_projects 
        WHERE category_id = 2 
        AND submitted_at >= CURRENT_DATE - INTERVAL '30 days'
        ORDER BY submitted_at DESC 
        LIMIT 5
    ");
    $stmt->execute();
    $recent_projects = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Active environmental initiatives
    $stmt = $pdo->query("
        SELECT * FROM clubs 
        WHERE category = 'environment' 
        AND status = 'active'
        ORDER BY name
        LIMIT 6
    ");
    $active_initiatives = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Upcoming environmental events list
    $stmt = $pdo->prepare("
        SELECT e.*, ec.name as category_name
        FROM events e
        LEFT JOIN event_categories ec ON e.category_id = ec.id
        WHERE e.category_id = 5
        AND e.event_date >= CURRENT_DATE
        AND e.status = 'published'
        ORDER BY e.event_date ASC, e.start_time ASC
        LIMIT 5
    ");
    $stmt->execute();
    $upcoming_events_list = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Recent activities (login activities)
    $stmt = $pdo->query("
        SELECT la.*, u.full_name, u.role 
        FROM login_activities la 
        JOIN users u ON la.user_id = u.id 
        ORDER BY la.login_time DESC 
        LIMIT 8
    ");
    $recent_activities = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Unread messages (PostgreSQL compatible)
    $stmt = $pdo->prepare("
        SELECT COUNT(*) as unread_count 
        FROM conversation_messages cm
        JOIN conversation_participants cp ON cm.conversation_id = cp.conversation_id
        WHERE cp.user_id = ? AND (cp.last_read_message_id IS NULL OR cm.id > cp.last_read_message_id)
    ");
    $stmt->execute([$user_id]);
    $unread_messages = $stmt->fetch(PDO::FETCH_ASSOC)['unread_count'] ?? 0;
    
    // Facility maintenance status
    $stmt = $pdo->query("
        SELECT 
            status,
            COUNT(*) as facility_count
        FROM sports_facilities 
        GROUP BY status
        ORDER BY facility_count DESC
    ");
    $facility_status = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Environmental project statistics
    $stmt = $pdo->query("
        SELECT 
            status,
            COUNT(*) as project_count
        FROM innovation_projects 
        WHERE category_id = 2
        GROUP BY status
        ORDER BY project_count DESC
    ");
    $project_status = $stmt->fetchAll(PDO::FETCH_ASSOC);
    
    // Get pending tickets count for this minister
    $stmt = $pdo->prepare("
        SELECT COUNT(*) as pending_tickets 
        FROM tickets 
        WHERE assigned_to = ? AND status IN ('open', 'in_progress')
    ");
    $stmt->execute([$user_id]);
    $pending_tickets = $stmt->fetch(PDO::FETCH_ASSOC)['pending_tickets'] ?? 0;
    
    // Get sidebar statistics
    $stmt = $pdo->query("SELECT COUNT(*) as total_cases FROM arbitration_cases");
    $total_cases = $stmt->fetch(PDO::FETCH_ASSOC)['total_cases'] ?? 0;
    
    $stmt = $pdo->query("SELECT COUNT(*) as pending_cases FROM arbitration_cases WHERE status IN ('filed', 'under_review')");
    $pending_cases = $stmt->fetch(PDO::FETCH_ASSOC)['pending_cases'] ?? 0;
    
    $stmt = $pdo->query("SELECT COUNT(*) as active_elections FROM elections WHERE status IN ('nomination', 'campaign', 'voting')");
    $active_elections = $stmt->fetch(PDO::FETCH_ASSOC)['active_elections'] ?? 0;
    
    $stmt = $pdo->query("SELECT COUNT(*) as recent_notes FROM case_notes WHERE created_at >= CURRENT_DATE - INTERVAL '7 days'");
    $recent_notes = $stmt->fetch(PDO::FETCH_ASSOC)['recent_notes'] ?? 0;
    
    $stmt = $pdo->query("SELECT COUNT(*) as recent_docs FROM case_documents WHERE created_at >= CURRENT_DATE - INTERVAL '7 days'");
    $recent_docs = $stmt->fetch(PDO::FETCH_ASSOC)['recent_docs'] ?? 0;
    
} catch (PDOException $e) {
    // Handle general error
    error_log("Minister of Environment & Security dashboard statistics error: " . $e->getMessage());
